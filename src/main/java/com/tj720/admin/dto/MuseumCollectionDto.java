	private String collectionRecord;//是否建立藏品档案(1全部藏品均建立了藏品档案；2珍贵文物全部建立了藏品档案；3部分一级以上珍贵文物全部建立了藏品档案;4否)
	private String manageSystem;//是否建立藏品管理制度（包括建账建档、提用养护、展厅库房、文物安全、应急预案等方面。）
	private String safetyOperation;//是否建立了藏品保护管理制度和安全操作规程(1:是；0否)
	private String storehouseManage;//是否建立库房管理制度(1是0：否)
	private String storehouseDiary;//是否建立了《库房日记》(1:是0：否)
	private String collectionMethod;//是否建立了藏品征集办法、征集工作流程(1是0否)
	private String standard;//是否建立了本馆入藏标准(1:是0：否)
	private String conservation;//是否建立了藏品养护办法(1:是0：否)
	private String  conservationRegular;//藏品是否定期养护(1:是0否)
	private String	academicBoard;//是否有学术委员会(1:是0：否)
	private String researchDepart;//是否有科研部门(1是0：否)
	private String laboratory;//是否拥有独立的科研室、实验室(1:是0：否)
	private String labAspect;//科研室、实验室方向
	
