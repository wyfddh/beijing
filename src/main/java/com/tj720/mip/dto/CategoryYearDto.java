package com.tj720.mip.dto;

import java.util.List;

import com.tj720.mip.model.YearType;

public class CategoryYearDto {
	
	private String categoryId;
	private String categoryName;
	private List<YearType> yearTypes;
	
	public String getCategoryId() {
		return categoryId;
	}
	public void setCategoryId(String categoryId) {
		this.categoryId = categoryId;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public List<YearType> getYearTypes() {
		return yearTypes;
	}
	public void setYearTypes(List<YearType> yearTypes) {
		this.yearTypes = yearTypes;
	}

}
