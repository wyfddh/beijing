package com.tj720.mip.dto;

import java.util.List;

import com.tj720.mip.model.MipOrganization;

public class CityMuseumDto {
	private String id;
	private String name;
	private List<MipOrganization> museum;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List<MipOrganization> getMuseum() {
		return museum;
	}
	public void setMuseum(List<MipOrganization> museum) {
		this.museum = museum;
	}
	

}
